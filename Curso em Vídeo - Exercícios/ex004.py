x = input("Digite um valor para x: ")
print("O tipo primitivo da variável x é {}".format(type(x)))
print("A variável x é numérica: {}".format(x.isnumeric()))
print("A variável x é alfabética: {}".format(x.isalpha()))
print("A variável x é alfanumérica: {}".format(x.isalnum()))
print("A variável x é maiúscula: {}".format(x.isupper()))
print("A variável x é minuscula?: {}".format(x.islower()))
print("A variável x está capitalizada?: {}".format(x.istitle()))



